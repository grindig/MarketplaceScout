"""ANSI color constants for terminal output."""

RESET      = "\033[0m"
BOLD       = "\033[1m"
DIM        = "\033[2m"
RED        = "\033[31m"
YELLOW     = "\033[33m"
CYAN       = "\033[36m"
MAGENTA    = "\033[35m"
GREEN      = "\033[92m"  # bright green
DARK_GRAY  = "\033[90m"
LIGHT_GRAY = "\033[37m"
